class SongInfo
{
    constructor(id, title, cover, author, band, album, category, duration, score, faixa, url){
        this.id = id;
        this.title = title;
        this.cover = cover;
        this.author = author;
        this.band = band;
        this.album = album;
        this.category = category;
        this.duration = duration;
        this.score = score;
        this.faixa = faixa;
        this.url = url;
    }
}

export {SongInfo}